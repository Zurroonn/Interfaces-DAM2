    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
 
        String elec="";
        String matriculado="";
        if (eleccion==curso.Primero) {
            elec="1ยบ";
        }else if(eleccion==curso.Segundo){
            elec="2ยบ";
        }else if (eleccion==curso.Ninguno) {
            elec="Ninguno";
        }else if (eleccion==curso.Tercero) {
            elec="Tercero";
        }
        if (jCheckBox1.isSelected()) {
            matriculado="No";
        }else{
            matriculado="Si";
        }
        jTextArea1.setText("Nombre: "+jTextField7.getText()+"\n"
                + "Curso: "+elec+"\n"
                + "Ciclo formativo: "+jComboBox2.getSelectedItem()+"\n"
                + "Matriculado: "+matriculado+"\n"
                + "Fecha Nacimiento: "+jSpinner2.getValue()+"\n"
                + "Edad: "+jSpinner1.getValue()+"\n"
                + "Provincia: "+jComboBox1.getSelectedItem()+"\n");
    }//GEN-LAST:event_jButton2ActionPerformed


    private void GuardarlistaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GuardarlistaMouseClicked
        // TODO add your handling code here:
        JFileChooser file= new JFileChooser();
        int eleccion=file.showSaveDialog(this);
        
        if (eleccion==JOptionPane.YES_OPTION) {
            
            try {
                File archivo= file.getSelectedFile();
                BufferedWriter oos = new BufferedWriter(new FileWriter(archivo));
                String con;
                int cont=1;
                for (Contacto contacto : contact) {
                    con="Contacto"+cont+": "+contacto.getNombre()+","+contacto.getEmail()+","+contacto.getTelefono()+","+contacto.getCategoria();
                    oos.write(con);
                    oos.newLine();
                    cont++;
                }
                oos.close();
                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Gestor.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Gestor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
    }//GEN-LAST:event_GuardarlistaMouseClicked


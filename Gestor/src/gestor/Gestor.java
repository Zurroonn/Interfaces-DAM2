    private void AñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AñadirActionPerformed
        // TODO add your handling code here:
        if (Nombref.getText().equalsIgnoreCase("")||Emailf.getText().equalsIgnoreCase("")||Telefonof.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this,"TIENES QUE AÑADIR TODA LA INFORMACIÓN");
        }else {
        String nombre=Nombref.getText();
        String email=Emailf.getText();
        String telefono=Telefonof.getText();
        String categoria=(String)Categoriaf.getSelectedItem(); //CHEKAR ERRORES
        Contacto cont= new Contacto(nombre,email,telefono,categoria);
        contact.add(cont);
        ((DefaultTableModel)Tabla.getModel()).addRow(new Object[]{nombre,email,telefono,categoria});
        
        }
        
    }//GEN-LAST:event_AñadirActionPerformed

